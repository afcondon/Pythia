-- |
-- Built-in Python FFI shims for the core libraries (prelude, effect,
-- console, and a few common extras). These cover what's needed to get
-- Prelude-using programs running; anything beyond is supplied by the
-- user via an @ffi-py/@ directory (copied into the output last, so user
-- files win over these).
--
-- Conventions mirrored from the JS foreign modules (and Jurist's shims):
--
--   * curried functions are chains of unary closures
--   * effects are zero-argument closures returning the result
--   * arrays are lists
--   * records are dicts
--   * chars are 1-character strings (as in JS)
--   * @Ordering@-style results use the generated tag-tuples' string tags
--   * FnN / EffectFnN / STFnN foreigns are genuine multi-arg functions
--   * shim function names are the identToPyName-mangled foreign idents,
--     and the generated module imports exactly those names
--
